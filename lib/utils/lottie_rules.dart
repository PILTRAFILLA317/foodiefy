List<String> _keywords(List<String> base) {
  final expanded = <String>{};
  for (final keyword in base) {
    final normalized = keyword.trim().toLowerCase();
    if (normalized.isEmpty) continue;
    expanded.add(normalized);

    final overrides = _keywordSynonyms[normalized];
    if (overrides != null) {
      for (final synonym in overrides) {
        final normalizedSynonym = synonym.trim().toLowerCase();
        if (normalizedSynonym.isNotEmpty) {
          expanded.add(normalizedSynonym);
        }
      }
    }

    for (final variant in _generateVariants(normalized)) {
      expanded.add(variant);
    }
  }

  return expanded.toList();
}

Iterable<String> _generateVariants(String keyword) {
  final variants = <String>{};
  if (!keyword.contains(' ')) {
    variants.addAll(_pluralForms(keyword));
    variants.addAll(_genderForms(keyword));
  } else {
    if (keyword.contains(' de ')) {
      variants.add(keyword.replaceAll(' de ', ' con '));
    }
    if (keyword.contains('-')) {
      variants.add(keyword.replaceAll('-', ' '));
    }
  }
  return variants;
}

Iterable<String> _pluralForms(String word) {
  final variants = <String>{};
  if (word.isEmpty) return variants;
  if (word.endsWith('s')) {
    variants.add(word);
    return variants;
  }

  if (_endsWithAny(word, ['ch', 'sh', 'x', 'z'])) {
    if (word.endsWith('z') && word.length > 1) {
      variants.add('${word.substring(0, word.length - 1)}ces');
    }
    variants.add('${word}es');
  } else if (word.endsWith('y') && word.length > 1 && _isConsonant(word[word.length - 2])) {
    variants.add('${word.substring(0, word.length - 1)}ies');
  } else if (_isVowel(word[word.length - 1])) {
    variants.add('${word}s');
  } else {
    variants.add('${word}es');
  }

  return variants;
}

Iterable<String> _genderForms(String word) {
  final variants = <String>{};
  if (word.endsWith('o')) {
    variants.add('${word}a');
  } else if (word.endsWith('a')) {
    variants.add('${word.substring(0, word.length - 1)}o');
  }
  return variants;
}

bool _isVowel(String char) => 'aeiouáéíóú'.contains(char);

bool _isConsonant(String char) => ! _isVowel(char);

bool _endsWithAny(String word, List<String> endings) =>
    endings.any((ending) => word.endsWith(ending));

